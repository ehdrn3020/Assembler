MAIN SEGMENT
     ASSUME CS:MAIN, DS:MAIN
	MOV CL, 0
	MOV DI, 0
LOOP1:	CMP STRING[DI], 'A'		
	JB  SMALL
	CMP STRING[DI], 'Z'
	JA  SMALL
	INC CL
	INC DI
	CMP DI,17
	JB  LOOP1

SMALL:  INC DI
	CMP DI,17
	JB  LOOP1

	MOV DL,CL
	ADD DL,'0'
	MOV AH,2
	INT 21H

	MOV AH, 4CH
	INT 21H
STRING DB 'Free As Tthe Wind'
MAIN ENDS
     END
